def test_event_mapping_fields(tmp_path):
    projects = tmp_path / "projects"
    _write_transcript(projects, "-home-zach-workspace-devrc", "abc123", [
        _user_line("hello", uuid="u-1", cwd="/home/zach/workspace/devrc", branch="feat/x"),
    ])
    ev = next(iter(T.iter_messages([str(projects)])))
    assert ev["source"] == "claude"
    assert ev["kind"] == "prompt"
    assert ev["project"] == "devrc"
    assert ev["session"] == "abc123"
    assert ev["app"] == "claude-code"
    assert ev["cwd"] == "/home/zach/workspace/devrc"
    assert ev["gitBranch"] == "feat/x"
    assert ev["ts"] is not None and ev["ts"][:4] == "2026"


def test_ts_conversion_to_ch_format():
    out = T.to_ch_ts("2026-06-24T01:25:00.759Z")
    # local-time string in CH DateTime64(3) format: 'YYYY-MM-DD HH:MM:SS.mmm'
    assert len(out) == len("2026-06-24 01:25:00.759")
    assert out[4] == "-" and out[10] == " " and out[19] == "."
    assert out.endswith(".759")


# --------------------------------------------------------------------------- #
# Idempotency
# --------------------------------------------------------------------------- #
@pytest.fixture
def env(tmp_path, monkeypatch):
    spool = tmp_path / "spool"
    spool.mkdir()
    state = tmp_path / "claude-state.json"
    projects = tmp_path / "projects"
    projects.mkdir()
    monkeypatch.setenv("ACTIVITY_SPOOL_DIR", str(spool))
    monkeypatch.setenv("CLAUDE_SOURCE_STATE", str(state))
    monkeypatch.setenv("CLAUDE_SOURCE_EMIT", str(EMIT))
    monkeypatch.setenv("CLAUDE_PROJECTS_DIR", str(projects))
    return {"spool": spool, "state": state, "projects": projects}


def _spool_events(spool: Path) -> list[dict]:
    cur = spool / "current.log"
    if not cur.exists():
        return []
    out = []
    for line in cur.read_text(encoding="utf-8").splitlines():
        ev = C.parse_line(line)
        if ev:
            out.append(ev)
    return out


def test_run_twice_emits_each_once(env):
    _write_transcript(env["projects"], "-home-zach-workspace-devrc", "s1", [
        _user_line("msg one", uuid="u-1"),
        _user_line("msg two", uuid="u-2"),
    ])
    assert T.run() == 0
    assert T.run() == 0  # second run must add nothing
    evs = _spool_events(env["spool"])
    assert len(evs) == 2
    assert sorted(e["text"] for e in evs) == ["msg one", "msg two"]


def test_append_new_message_emits_only_new(env):
    p = _write_transcript(env["projects"], "-home-zach-workspace-devrc", "s1", [
        _user_line("first", uuid="u-1"),
    ])
    T.run()
    assert len(_spool_events(env["spool"])) == 1
    # append a new message + re-run
    with p.open("a", encoding="utf-8") as f:
        f.write(_user_line("second", uuid="u-2") + "\n")
    T.run()
    evs = _spool_events(env["spool"])
    assert len(evs) == 2
    assert sorted(e["text"] for e in evs) == ["first", "second"]


def test_state_file_round_trips(env):
    _write_transcript(env["projects"], "-home-zach-workspace-devrc", "s1", [
        _user_line("hello", uuid="u-1"),
    ])
    T.run()
    seen = T.load_state(env["state"])
    assert "u-1" in seen
    # round-trip through save/load
    T.save_state(env["state"], seen | {"u-extra"})
    seen2 = T.load_state(env["state"])
    assert {"u-1", "u-extra"} <= seen2


def test_uuidless_line_deduped_by_content(env, monkeypatch):
    # Build a line with no uuid and confirm two runs emit it once.
    obj = {
        "type": "user", "timestamp": "2026-06-24T01:25:00.759Z",
        "cwd": "/home/zach/workspace/devrc", "gitBranch": "main",
        "message": {"role": "user", "content": "no uuid here"},
    }
    d = env["projects"] / "-home-zach-workspace-devrc"
    d.mkdir(parents=True, exist_ok=True)
    (d / "s1.jsonl").write_text(json.dumps(obj) + "\n", encoding="utf-8")
    T.run()
    T.run()
    assert len(_spool_events(env["spool"])) == 1


