def test_typed_message_extracted():
    assert T.classify("please refactor the parser") == ("typed", "please refactor the parser")


def test_slash_command_becomes_command_kind():
    raw = "<command-message>handoff is running…</command-message><command-name>handoff</command-name><command-args>now</command-args>"
    kind, text = T.classify(raw)
    assert kind == "command"
    assert text == "handoff now"


def test_system_reminder_filtered_out():
    raw = "<system-reminder>internal note that is fairly long and synthetic content</system-reminder>"
    assert T.classify(raw) is None


def test_local_command_stdout_stripped():
    raw = "<local-command-stdout>some tool output here padding padding</local-command-stdout>"
    assert T.classify(raw) is None


def test_boilerplate_prefixes_filtered():
    assert T.classify("[Request interrupted by user]") is None
    assert T.classify("Caveat: The messages below were generated by the user") is None
    assert T.classify("API Error: 500 overloaded") is None


def test_system_reminder_stripped_but_real_text_kept():
    raw = "<system-reminder>noise noise noise noise noise</system-reminder>\nreal question here"
    assert T.classify(raw) == ("typed", "real question here")


def test_content_as_list_blocks_handled():
    content = [
        {"type": "tool_result", "content": "ignored"},
        {"type": "text", "text": "first text block"},
        {"type": "image"},
        {"type": "text", "text": "second text block"},
    ]
    blocks = T.extract_blocks(content)
