def test_emitted_lines_parse_back_via_collector(env):
    _write_transcript(env["projects"], "-home-zach-workspace-devrc", "sess-xyz", [
        _user_line('typed "with quotes"\nand newline 🚀', uuid="u-typed",
                   cwd="/home/zach/workspace/devrc", branch="feat/claude-src"),
        _user_line(
            "<command-name>handoff</command-name><command-args>now</command-args>",
            uuid="u-cmd"),
    ])
    assert T.run() == 0
    evs = _spool_events(env["spool"])
    by_text = {e["text"]: e for e in evs}

    typed = by_text['typed "with quotes"\nand newline 🚀']
    assert typed["source"] == "claude"
    assert typed["kind"] == "prompt"
    assert typed["project"] == "devrc"
    assert typed["session"] == "sess-xyz"
    assert typed["app"] == "claude-code"
    payload = json.loads(typed["payload"])
    assert payload["gitBranch"] == "feat/claude-src"
    assert payload["role"] == "user"
    assert payload["claude_kind"] == "typed"

    cmd = by_text["handoff now"]
    assert cmd["kind"] == "command"
    assert json.loads(cmd["payload"])["claude_kind"] == "command"


def test_emitted_ts_is_message_time_not_now(env):
    _write_transcript(env["projects"], "-home-zach-workspace-devrc", "s1", [
        _user_line("old message", uuid="u-1", ts="2020-01-02T03:04:05.678Z"),
    ])
    T.run()
    ev = _spool_events(env["spool"])[0]
    # The ts reflects the (local-converted) message time, not the current run
    # time. Compare against the tailer's own conversion of the message stamp.
    assert ev["ts"] == T.to_ch_ts("2020-01-02T03:04:05.678Z")
    assert ev["ts"].startswith("2020-01-0")  # year 2020, not 2026 (run time)
