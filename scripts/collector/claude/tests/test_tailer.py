"""Tests for the claude-source tailer (scripts/collector/claude/tailer.py).

Covers:
  * extraction / noise-filtering (typed vs slash-command vs synthetic noise,
    content-as-list blocks, unicode/multiline/quotes survival),
  * idempotency (run twice → each message once; append → only the new one;
    state file round-trips),
  * emit-format correctness — the emitted spool lines parse back via the real
    collector's parse_line to the expected event dict.

No network. The real `emit` shell helper is invoked (writes to a temp spool),
and the real collector module is imported by sys.path like test_collector.py.
"""
import json
import os
import sys
from pathlib import Path

import pytest

# Import the tailer (this dir's parent) and the collector daemon (sibling dir),
# mirroring how scripts/collector/tests/test_collector.py imports collector.
_CLAUDE_DIR = Path(__file__).resolve().parent.parent          # scripts/collector/claude
_COLLECTOR_DIR = _CLAUDE_DIR.parent                            # scripts/collector
sys.path.insert(0, str(_CLAUDE_DIR))
sys.path.insert(0, str(_COLLECTOR_DIR))
import tailer as T      # noqa: E402
import collector as C   # noqa: E402

EMIT = _COLLECTOR_DIR / "emit"


# --------------------------------------------------------------------------- #
# Transcript fixture helpers
# --------------------------------------------------------------------------- #
def _user_line(text_or_blocks, *, uuid, ts="2026-06-24T01:25:00.759Z",
               cwd="/home/zach/workspace/devrc", branch="main",
               isMeta=False, isSidechain=False, role="user"):
    obj = {
        "type": "user",
        "uuid": uuid,
        "timestamp": ts,
        "cwd": cwd,
        "gitBranch": branch,
        "isMeta": isMeta,
        "isSidechain": isSidechain,
        "message": {"role": role, "content": text_or_blocks},
    }
    return json.dumps(obj)


def _write_transcript(projects_dir: Path, project_dirname: str, session: str, lines):
    d = projects_dir / project_dirname
    d.mkdir(parents=True, exist_ok=True)
    p = d / f"{session}.jsonl"
    p.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return p


